  case DEV_AUTH_PRIV_KEY:
    *out = _DEV_AUTH_PRIV;
    break;
  case DEV_AUTH_SERVER_KEY:
    *out = _DEV_AUTH_PUB;
    break;
  case FW_VERIFICATION_KEY:
