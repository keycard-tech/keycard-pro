app_err_t key_read_private(key_type_t type, uint8_t* out) {
  switch(type) {
  case DEV_AUTH_PRIV_KEY:
    hal_derive_device_unique_secret(_DEV_AUTH_SALT, out);
    break;
  default:
    return ERR_DATA;
  }

  return ERR_OK;
}

app_err_t key_read_public(key_type_t type, const uint8_t** out) {
  switch(type) {

  case DEV_AUTH_SERVER_KEY:
    *out = _DEV_AUTH_SERVER;
    break;
  case FW_VERIFICATION_KEY:
    *out = (uint8_t*)(HAL_FLASH_ADDR + HAL_FW_HEADER_OFFSET);
    break;
  case DB_VERIFICATION_KEY:
    *out = _DB_PUB;
    break;
  case KEYCARD_CA_KEY:
    *out = _KEYCARD_CA_PUB;
    break;
  default:
    return ERR_DATA;
  }

  return ERR_OK;
}
